<p>TJ Aerospace is a growing Aerospace manufacturing company specializing in milling/fabricating small to large high precision-machined and tight tolerance parts in Garden Grove, California.</p>



<h4>Job Summary</h4>



<p>The Swiss Machinist is responsible for the daily operations
of the machines in their area (Milling, Turning, Mill/Turn, Swiss and EDM) and the
quality of the end product.&nbsp; 20%
Differential.</p>



<h4>Essential Duties and Responsibilities:</h4>



<p><em>The primary duties and responsibilities of the Swiss Machinist consist of but are not limited to:</em></p>



<ul><li>Operation of machines in one machining department</li><li>Perform simple setups of machines in designated area</li><li>Perform in-process inspection of machined products (including documentation of results)</li><li>Complete and maintain paperwork for parts</li><li>Complete secondary operations i.e. deburr, polish, etc. in cycle time per print and process</li><li>Ability to read and interpret prints</li><li>Troubleshoot with the support from a Level III or IV Machinist, Supervisor or Engineer</li><li>Perform total productive machine maintenance</li><li>Maintain balance when completing machine set-up, production and maintenance duties</li><li>Continuous use of hands and upper extremities in fine and gross manipulation using various wrist positions and grip/pinch spans to operate equipment, push buttons, carry boxes, push carts, etc.</li><li>Frequent grip with tools requiring torque to tighten machine parts</li><li>Lifting parts/tools</li><li>Prolonged standing with occasional twisting (80-90% of the workday)</li><li>Able to lift up to 70lbs</li><li>Must be able to meet customer demand requirements for performing visual inspection and/or other product acceptance activities with or without accommodation</li><li>Exhibit a working knowledge of health and safety, responsible for safety awareness, while upholding and improving the culture of safety within the organization.  Identify opportunities for improvement and take action if appropriate, before a problem occurs.  Report potential hazards, unsafe working conditions, unsafe equipment, unsafe acts and injuries immediately</li><li>Other tasks as assigned</li></ul>



<h4>Qualifications:</h4>



<ul><li>Minimum Requirements: H.S. Diploma or equivalent</li><li>Required: Two-year certificate in machine trades</li><li>Preferred: 5+ yearsâ€™ experience in CNC machining</li></ul>



<h4>Accountabilities:</h4>



<ul><li>Detects and reports defective materials or
questionable conditions to the department manager</li><li>Perform to established production standards</li><li>Maintain work area and equipment in a clean
orderly condition and follows prescribed safety procedures at all times</li></ul>



<h4>Authorities:</h4>



<ul><li>Stop the machine or process if safety, quality,
or productivity standards are compromised</li></ul>



<h4>Competencies:</h4>



<ul><li>Able to read and interpret blueprints, use simple math to perform basic calculations and use hand-held measuring tools</li><li>Basic understanding of GD&amp;T</li><li>Demonstrated knowledge of SPC</li><li>Understanding of tools required to check products to guarantee part quality</li><li>Experience using manual inspection equipment</li><li>Knowledge of CMM &amp; VCMM</li><li>Strong mechanical aptitude</li><li>Demonstrated quality record</li><li>Excellent attention to detail</li><li>Strong written and verbal communication skills</li><li>Proficient in various computer software applications such as MS Word, Excel and Outlook</li><li>Excellent problem-solving skills</li><li>Able to work efficiently with minimal supervision</li><li>Able to work both independently and as a team</li><li>Self-starter with great analytical skills</li></ul>

