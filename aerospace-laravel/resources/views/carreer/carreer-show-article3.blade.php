<h4>Job Summary</h4>



<p>TJ Aerospace is a growing Aerospace manufacturing company specializing in milling/fabricating small to large high precision-machined and tight tolerance parts in Garden Grove, California.</p>



<h4>Essential Duties and Responsibilities:</h4>



<p><em>The primary duties and responsibilities of the Inventory Controller consist of but are not limited to:</em></p>



<ul><li>Maintains inventory
(Inventory Room, Hardware Room, Raw Material Stock) by identifying, labeling
and placing materials and supplies in stock; recording location of inventory.</li><li>Stores product by
designating storage areas; adjusting storage areas according to production and
shipping requirements; updating location database.</li><li>Locates materials and
supplies by pulling and verifying materials and supplies listed on production
orders.</li><li>Maintains in-process
inventory at work centers by delivering and opening materials and supplies.</li><li>Documents materials and
supplies disposition by recording units delivered and location of units.</li><li>Verifies product
inventory reports by comparing logs and reports; adjusting entries.</li><li>Prepares orders by
processing requests and supply orders; pulling materials; packing boxes;
placing orders in delivery area.</li><li>Documents actions by
completing forms, reports, logs and records; maintaining databases.</li><li>Monitors inventory by
completing counts as required.</li><li>Receives credit-return
material and supplies from production by verifying materials and supplies code
and lot number and quantity; placing materials in stock.</li><li>Prepares finished stock
for shipment by identifying, pulling, packing, crating, loading and securing
product.</li><li>Prepares product for
shipment by verifying location; directing movement to staging area; resolving
problems.</li><li>Documents product
shipment by recording units shipped.</li><li>Maintains
material-handling equipment by completing pre-use inspections; making operator
repairs.</li><li>Picks-up product by
monitoring production; loading and moving product with a forklift.</li><li>Completes deliveries by
driving truck or van to and from vendors.</li><li>Maintains quality
service by following organization standards.</li><li>Maintains safe and
clean work environment by keeping shelves, pallet area and workstations neat;
maintaining clean shipping supply area; complying with procedures, rules and
regulations.</li><li>Contributes to team
effort by accomplishing related results as needed.</li></ul>



<h4>Desired Skills and Experience:</h4>



<ul><li>Experience in a high
volume manufacturing environment: AS9100 and First Article Inspection</li><li>Organization; analyzing
information; deal with complexity; detail-oriented; deadline-oriented; time
management; process improvement; safety management; lifting; data entry skills</li><li>Knowledge of
scheduling, production workflow and planning procedures</li><li>Ability and knowledge
to operate a forklift</li><li>Familiar with ERP
programs such as JobBoss</li><li>Working knowledge of
Microsoft Office</li><li>Intermediate math
skills</li><li>Ability to multi-task</li></ul>



<p>Experience with multiple aerospace customer specifications.</p>

